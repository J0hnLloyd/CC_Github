function setup() {
  createCanvas(400, 400);
}

function draw() {
  background(0); // Dark background for space-like effect...

  // Head...
  fill(150); // Grey color for the alien's head...
  ellipse(200, 150, 120, 150); // Head...

  // Three Eyes...
  fill(0); // Color for eyes...
  ellipse(160, 140, 30, 50); // Left...
  ellipse(200, 140, 30, 50); // Middle...
  ellipse(240, 140, 30, 50); // Right...

  fill(255); // Eye highlights...
  ellipse(165, 140, 10, 15); // Left Eye highlights...
  ellipse(200, 140, 10, 15); // Middle Eye highlights...
  ellipse(235, 140, 10, 15); // Right Eye highlights...

  // Antenna...
  strokeWeight(1);
  stroke(35, 33, 36);
  line(200, 50, 200, 75); // Center antenna

  fill(51, 32, 61); // Antenna color tip...
  ellipse(200, 40, 20, 20);

  // Smiling Mouth...
  noFill();
  strokeWeight(3);
  stroke(0); // Color for mouth...
  beginShape();
  vertex(180, 200); // Start point of the mouth
  bezierVertex(185, 210, 215, 210, 220, 200); // Bezier curve for the smile
  endShape();
}
